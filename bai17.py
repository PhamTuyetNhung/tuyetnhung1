a=int(input("Nhập số nguyên a:"))
b=int(input("Nhập số nguyên b:"))
c=int(input("Nhập số nguyên c:"))
solonnhat=max(a,b,c)
sonhonhat=min(a,b,c)
print("Số lớn nhất là:",solonnhat)
print("Số nhỏ nhất là:",sonhonhat)